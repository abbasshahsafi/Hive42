int	ft_iterative_power(int nb, int power)
{
	int	i;

	i = nb;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	while (power > 1)
	{
		nb *= i;
		power--;
	}
	return (nb);
}

#include <stdio.h>

int main(void)
{
	printf("%i", ft_iterative_power(2, 2 ));
}
