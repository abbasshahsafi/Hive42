#include "ft_stock_str.h"
#include <stdlib.h>

int	ft_str_length(char *str)
{
	int	index;

	index = 0;
	while (str[index])
		index++;
	return (index);
}

char	*ft_strdup(char *src)
{
	int		index;
	char	*dest;

	index = 0;
	dest = (char *)malloc(ft_str_length(src) * sizeof(char) + 1);
	if (!dest)
		return (NULL);
	while (src[index])
	{
		dest[index] = src[index];
		index++;
	}
	dest[index] = '\0';
	return (dest);
}

struct s_stock_str	*ft_strs_to_tab(int ac, char **av)
{
	int					index;
	struct s_stock_str	*array;

	array = (struct s_stock_str *)malloc((ac + 1) * sizeof(struct s_stock_str));
	if (!array)
		return (NULL);
	index = 0;
	while (index < ac)
	{
		array[index].size = ft_str_length(av[index]);
		array[index].str = av[index];
		array[index].copy = ft_strdup(av[index]);
		if (array[index].copy == NULL)
		{
			while (index > 0)
			{
				index--;
				free(array[index].copy);
			}
			free(array);
			return (NULL);
		}
		index++;
	}
	array[index].str = 0;
	array[index].copy = 0;
	return (array);
}
